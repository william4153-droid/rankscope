using ((select auth.uid()) = user_id);

revoke all on function public.reserve_keyword_search() from public, anon;
grant execute on function public.reserve_keyword_search() to authenticated;

revoke all on function public.release_keyword_search(uuid) from public, anon, authenticated;
grant execute on function public.release_keyword_search(uuid) to service_role;

